                                            <div class="post-content-inner">
                                                <p>‘Innocent until proven guilty’ is one of the founding principles of the UK justice system and a key part of upholding this is the accumulation and presentation of clear, factual evidence.</p>

                                                <p>
                                                    This is especially important with regards to medical evidence when charging and detaining criminal suspects. This evidence can be the deciding factor in a wide range of cases such as murder charges, domestic violence trials or
                                                    terror offences, so ensuring this information can be easily gathered and is accessible is vital.
                                                </p>

                                                <p>
                                                    However, securing this evidence isn’t a simple process and comes with many challenges. Currently, medical information is obtained from the NHS through individual clinicians, who are contracted by the police to write medical
                                                    reports and translate hospital notes into court ready evidence on-site, often in their spare time and after long shifts.
                                                </p>

                                                <p>
                                                    This can be time consuming and it often takes two to three months for the police to receive the medical evidence needed to charge or detain individuals. The extended time it takes to receive medical reports can result in delays
                                                    in charging decisions and trials, leading to the premature release of potentially dangerous suspects who then have the freedom to re-offend.
                                                </p>

                                                <p>
                                                    What’s more, in 90 per cent of cases, doctors who are spending their own time preparing medical reports, do not get paid for their work; while one in eight reports are returned for poor quality because doctors are not trained to
                                                    write them. With a lack of training and payment, it’s not surprising that many clinicians are reluctant to take on the task of preparing medical evidence, inevitably leading to delays.
                                                </p>

                                                <p>If we are to keep the public safe and ensure justice is served, we need to reform the way we collect medical evidence to ensure it is being delivered in the most fair, effective and efficient way possible.</p>

                                                <p>
                                                    To do this, the reporting process needs to be streamlined and harnessing external expertise could be the solution. For example, police usually have to manage the whole process of report preparation, securing a doctor to compile
                                                    it, chasing them for the evidence and ensuring it’s of acceptable quality. At the same time, already overworked doctors have to find time in their busy schedules to conduct detailed analysis and review large amounts of
                                                    information in order to create complex reports.
                                                </p>

                                                <p>
                                                    At a time when both the police and healthcare is under huge pressure due to the global pandemic, we need to find a better way to collect and report medical evidence. One way to do this would be to streamline the process through
                                                    a third party. This would drastically reduce the pressure on clinicians, and the time spent by police forces. External organisations that have medical evidence expertise can act as an intermediary between police and doctors,
                                                    collecting data and information from medics, creating high-quality reports and sharing these with police forces to charge suspects with crimes. This would reduce the burden on clinicians as they would not have to spend hours of
                                                    their own time collating medical evidence, while at the same time ensure the police receive an oven-ready report in a timely manner.
                                                </p>

                                                <p>
                                                    This process also allows police forces to better protect the general public. Ensuring medical reports are received at the earliest point and ‘right first time’ means that officers can focus their resources on front line police
                                                    work, and prosecute suspects sooner, reducing the risk of potentially dangerous criminals being released and allowed to commit more crimes.
                                                </p>

                                                <p>
                                                    Police forces work incredibly hard to ensure justice is served swiftly and fairly and to keep us safe. Through working with organisations that can remove some of the obstacles that obstruct them doing this, such as the managing
                                                    and preparation of medical evidence, it will provide them with the time and resources to do what they do best and ensure criminals are prosecuted quickly and our streets remain safe now and in the future.
                                                </p>
