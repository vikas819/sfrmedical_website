                                <p>We ensure justice to victims of violent crimes by reforming the complex and inefficient processes of obtaining medical evidence. Our service produces medical reports - of an excellent quality – quickly, and is a more cost-effective solution for UK police forces. Our Executive Team have experienced first-hand the frustrations of police officers, NHS staff and victims, when the production, quality and payment of medical evidence - prior to SFR Medical – faced multiple difficulties.</p>

                                <p>There are many victims of violent crime each year in the UK; between March 2019 and March 2020 alone, there were <a href="https://www.ons.gov.uk/peoplepopulationandcommunity/crimeandjustice/articles/thenatureofviolentcrimeinenglandandwales/yearendingmarch2020" target="_blank">1.8 million violence-against-a-person offences recorded</a> by UK police. When a violent crime occurs, medical evidence (comprising of medical notes and medical reports) is often required to form part of the case file submitted to the Crown Prosecution Service (CPS), to aid the charging decision of a suspect.</p>

                                <p>There are a multitude of problems with the traditional method of obtaining medical evidence (prior to SFR Medical). Police officers often do not know who to contact at the relevant medical institution and NHS administrative staff have to chase doctors who move posts and hospitals on a regular basis during their training. NHS medical professionals struggle to find the time to complete medical reports (they are not contracted to complete them within their clinical hours) and payment for these reports are irregular and difficult to manage. The completed medical reports are, more often than not, of a poor quality (handwritten with poor legibility, contained medical jargon and unstructured content). Officers would often need to be obtain repeated reports from every doctor that had cared for the patient and the authors would not know what the police and CPS would require from a report. Most importantly however, the combined effect of the above problems lead to a lengthy delay in the production of the medical evidence for the police (our research indicated between three and six months). This can induce increased anxiety for victims and reduced confidence in policing which may lead to witness attrition. Uncharged suspects lead to reoffending and an increased incident of revenge crimes (for instance, between opposing gang members).</p>

                                <p>Experiencing these frustrations as part of the everyday workings of the NHS, our Executive Team piloted a process which formed the origins of our now efficient SFR Medical process, resulting in a 85% reduction in waiting times, increased confidence in local policing and less frustration for the hospitals. Following the great success of this pilot, SFR Medical was founded.</p>

                                <p>Since SFR Medical’s inception, our team has grown, our operational processes have been enhanced and refined, our cybersecurity strengthened to the highest standards and our product offering list expanded.</p>

                                <p>However, what remains at SFR Medical’s core is our vision to create a world where victims of violent crimes are supported through robust technology, compassion and accurate data. To go beyond evidence. </p>




