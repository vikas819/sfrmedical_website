                                                <p>Medical negligence claims involving children often arise from delayed diagnosis of common illnesses or errors in treatment. Children, especially young ones, are vulnerable to receiving incorrect treatment or being misdiagnosed due to their inability to fully communicate their symptoms. In UK, children are not allowed to conduct legal proceedings on their own (i.e., without a parent or an appointed guardian) until they reach the age of 18. Therefore, in personal injury and clinical negligence claims involving children, solicitors will be in direct contact and instructed by the litigation friend (i.e., the parent/legal guardian of the child).  </p> 

                                                <p>When handling personal injury and clinical negligence claims involving children, solicitors must take into account some key legal considerations, starting with the limitation periods. In the UK, the general limitation period for personal injury claims is three years from the date of the incident or from the date of knowledge of the injury. However, when it comes to children, the three-year limitation period does not start until their 18th birthday. Therefore, it is important for solicitors to be aware of the time limits for bringing a claim on behalf of a child. Nonetheless, it is always advisable to start the claim as soon as possible after the incident occurred. </p>

                                                <p>Moreover, since children under the age of 16 cannot give valid consent to medical treatment, solicitors must ensure that proper consent was obtained before any medical treatment was carried out and that the parents or guardians were fully informed of the risks involved. </p>

                                                <p>Additionally, the solicitors must be careful in assessing and estimating the damages that the child is entitled to. When it comes to clinical negligence claims, the standard of care expected of healthcare professionals is the same whether the patient is a child or an adult. However, children are considered more vulnerable; therefore, the duty of care owed to a child is higher than that owed to an adult. Thus, the damages shall not only include the present cost of medical treatment, but also any future care costs. In cases where the consequences of a child’s injury cannot be predicted until he/she is older, the solicitor should pursue “interim payments” (i.e., ongoing payments that meet the child’s need until the claim is finalized). If the settlement of a personal injury or clinical negligence claim involving a child is worth more than £10,000, the settlement must be approved by the court. Solicitors must ensure that all necessary documentation has been prepared and presented to the court, and that the settlement is in the best interests of the child. The compensation can be put into an investment/savings account that the child can access after turning 18 years old.  </p>

                                                <p>Furthermore, the solicitor must be able to prove the causation between the injury/negligence suffered by the child and the actions of the defendant, to establish a breach of duty in a clinical negligence claim. For this, solicitors will usually need to obtain expert evidence. In cases involving children, the expert should have specific knowledge and experience in treating children. </p>

                                                <p>Lastly, it would be highly appreciated if the solicitor had a good understanding of funding options available for personal injury and clinical negligence claims involving children. These might include - among others -  legal aid, conditional fee agreements (“no win, no fee”), and third-party funding. </p>

                                                 

