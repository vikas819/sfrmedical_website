                                                <p>As I medical doctor, I – and my colleagues – focus on providing excellent medical care to victims, but unfortunately, usually actively avoid getting involved with providing forensic evidence to facilitate court proceedings.</p>

                                                <p>However, there is a conflict of expectations that we face every day. On the one hand, the justice system expects and often demands our involvement. The documentation we produce is written in a medical language aimed at communicating with colleagues, not to the layman sitting in a jury stand. As such, our internal papers need to be translated into everyday language that everyone can understand, into what we call medical evidence.</p>

                                                <p>On the other hand, the NHS does not consider providing medical evidence as part of our contractual responsibilities nor are they willing to pay us for this additional work. When we are asked to prepare these documents, it’s often after a long, emotionally draining shift, and the potential to make a mistake is high.</p>

                                                <p>You can see the dilemma. This conflict makes providing medical evidence a complex and challenging process.</p>

                                                <p>This is why we founded SFR Medical.</p>

                                                <p>Medical evidence remains a crucial part of the process and the lack thereof will cause thousands of criminal cases to collapse. Victims will suffer again, augmented by a justice system that will let them down.</p>

                                                <p>SFR Medical has streamlined and optimised the way in which police forces receive forensic medical reports from hospitals when someone has been assaulted. We provide it in a timely and efficient manner, through a process that is convenient and easy for both the police and the hospitals. We significantly reduce the burden for all involved, leading to faster processing of suspects. We are a small but essential cog in the wheel of justice.</p>
