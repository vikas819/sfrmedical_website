                                                <p>SFR Medical was founded on the principle of ensuring justice for all, and this mission drives everything we do.</p>

                                                <p>Our position is simple:</p>

                                                <ul>
                                                    <li>We stand against racial injustice and failures of criminal justice, regardless of who the victim is, where they are from, what their profession is, or the color of their skin</li>
                                                    <li>We believe in peaceful protests for positive change, a change that can reshape the world we live in for the better</li>
                                                    <li>We support our black colleagues, partners, clients, and friends</li>
                                                </ul>

                                                <p>Silence is no longer acceptable.</p>

                                                <p>We promise to keep working and learning. We pledge to include discussions about inequality when thinking about how we, as a business and as individuals, spend time and money, and allocate resources.</p>

                                                <p>But we can’t do this alone – we need to work together to make meaningful change not only happen but stick.</p>

                                                <p>So let’s keep working. Let’s stand for justice. Let’s stand together and say it loud and clear: #BlackLivesMatter #WeStandforJustice</p>

