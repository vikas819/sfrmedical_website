                                            <div class="post-content-inner">
                                                <p>The COVID-19 pandemic has disrupted countless industries since it emerged last year and one area where it is having a particularly damaging effect is on our criminal justice system.</p>

                                                <p>
                                                    The true impact of the virus on the UK judiciary came to light recently when the inspectorates for policing, prisons, probation and prosecutions <a href="https://www.bbc.co.uk/news/uk-55712106">voiced their concerns</a> about
                                                    the impact of court backlogs caused by the pandemic. This was after figures revealed the backlog in the crown courts had reached 54,000 unheard cases, meaning some crimes committed in 2020 will not go before a jury before 2022.
                                                </p>

                                                <p>
                                                    This backlog of cases could drastically hamper victims’ pursuit of justice and leave the public vulnerable. Potentially dangerous suspects could remain free to commit further offences for some time before being tried and
                                                    convicted, while victims may pull-out of participating in court cases due to the long waiting times before their case is heard.
                                                </p>

                                                <p>We need to act now if we are to minimise the backlog of cases to ensure justice is served and one way to help achieve this is through streamlining processes to ensure they are as quick and efficient as possible.</p>

                                                <p>One area where streamlining could be particularly beneficial is in medical evidence reporting. Securing medical evidence to charge and detain suspects isn’t a simple process and comes with many challenges.</p>

                                                <p>
                                                    Currently, medical information is obtained from the NHS through individual clinicians, who are contracted by the police to write medical reports and translate hospital notes into court ready evidence on-site, often in their
                                                    spare time and after long shifts.
                                                </p>

                                                <p>
                                                    This can be time consuming and it often takes two to three months for the police to receive the medical evidence, resulting in delays to charging decisions and trials and leading to the premature release of potentially dangerous
                                                    suspects who then have the freedom to re-offend.
                                                </p>

                                                <p>
                                                    What’s more, in up to 90 per cent of cases, doctors who are spending their own time preparing medical reports, do not get paid for their work; while one in eight reports are returned for poor quality because doctors are not
                                                    trained to write them. With a lack of training and payment, it’s not surprising that many clinicians are reluctant to take on the task of preparing medical evidence, inevitably leading to delays.
                                                </p>

                                                <p>If we want to reduce the backlog of cases caused by COVID-19 and keep the public safe, we need to reform the way we collect medical evidence to ensure it is being delivered in the most effective and efficient way possible.</p>

                                                <p>
                                                    To do this, the reporting process needs to be streamlined and harnessing external expertise could be the solution. For example, police usually have to manage the whole process of report preparation, securing a doctor to compile
                                                    it, chasing them for the evidence and ensuring it’s of acceptable quality. At the same time, already overworked doctors have to find time in their busy schedules to conduct detailed analysis and review large amounts of
                                                    information in order to create complex reports.
                                                </p>

                                                <p>
                                                    At a time when both the police and healthcare system are under huge pressure due to the pandemic, we need to find a better way to collect and report medical evidence. One way to do this would be to streamline the process through
                                                    a third party. External organisations that have medical evidence expertise can act as an intermediary between police and doctors, collecting data and information from medics, creating high-quality reports and sharing these with
                                                    police forces to charge suspects with crimes. This would reduce the burden on clinicians as they would not have to spend hours of their own time collating medical evidence, while at the same time ensure the police receive an
                                                    oven-ready report in a timely manner.
                                                </p>

                                                <p>
                                                    Ensuring medical reports are received at the earliest point and ‘right first time’ means that police forces can prosecute suspects sooner, helping cases progress more quickly and reducing the risk of potentially dangerous
                                                    criminals being released and allowed to commit more crimes. As we navigate our way out of the pandemic there will be huge challenges ahead to address the damage it has caused and this is certainly true when it comes to our
                                                    criminal justice system. If we want to limit the backlog of criminal cases, ensure victims receive justice and criminals are convicted, we need to act now to make sure processes such as medical evidence reporting are as
                                                    efficient as possible. This way we can tackle the long list of cases and keep people as safe as possible, now and in the future.
                                                </p>
